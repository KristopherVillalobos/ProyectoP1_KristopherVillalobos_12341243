    private int idrandom() {
        Random random = new Random();
        return random.nextInt(50) + 1;
    }
    
    private int ObtenerPosicion(String Id) {
        for (int i = 0; i < lista.size(); i++) {
            Menu t = lista.get(i);
            if (Id.equalsIgnoreCase(t.getIdplato())) {
                return i;
            }
        }
        return -1;
    }
    
